# Q. What is Python?
# A. •Python is a dynamically typed, general purpose programming language that supports an
#     object-oriented programming approach as well as a functional programming approach.
#   •Python is an interpreted and a high-level programming language.
#   •It was created by Guido van Rossum in 1989.

# Q. Feactures of Python
# A. •Python is simple and easy to understand.
#    •It is interpreted and platform-independent which makes debugging very easy.
#    •It is free and open source.
#    •Python provides very big library support. Some of the popular libraries includes NumPy, Matplotlib, Pandas, Scikit-learn, etc.
#    •It is possible to integrate other languages like C, C++, Java, etc. into Python.

# Q. What is Python used for?
# A. •Python is used for web development, software development, mathematics, system scripting, etc.
#    •Python is also used for data analysis, machine learning, and data visualization.
#    •It is used in AI and Machine Learning to simulate human behavior and to learn from past data without hard coding.

# Q. How can we install Python?
# A. •Python can be downloaded from the Python website. Python is available for Windows, Mac, and Linux.

